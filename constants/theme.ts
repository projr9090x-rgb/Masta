// Color palette interface
export interface ColorPalette {
  // Primary
  primary: string;
  primaryLight: string;
  primaryDark: string;
  
  // Backgrounds
  background: string;
  backgroundSecondary: string;
  backgroundTertiary: string;
  
  // Text
  text: string;
  textSecondary: string;
  textTertiary: string;
  
  // Priority colors
  priorityHigh: string;
  priorityHighBg: string;
  priorityMedium: string;
  priorityMediumBg: string;
  priorityLow: string;
  priorityLowBg: string;
  
  // Status
  success: string;
  successBg: string;
  error: string;
  errorBg: string;
  warning: string;
  warningBg: string;
  info: string;
  infoBg: string;
  
  // UI
  border: string;
  divider: string;
  card: string;
  overlay: string;
  overlayLight: string;
  
  // Premium
  premium: string;
  premiumGradient: [string, string];
}

